-- Creazione DB
CREATE DATABASE ToysGroups;
USE ToysGroups;

-- Creazione tabelle
CREATE TABLE DimProducts (
	ProductID CHAR(7) PRIMARY KEY NOT NULL,
    Name VARCHAR(25),
    CategoryName VARCHAR(25),
    Productor VARCHAR(50),
    ProductionCost DECIMAL(10,2),
    Price DECIMAL(10,2),
    StartingAgeOfUse TINYINT,
    Description VARCHAR(400),
    QuantityInStock SMALLINT
);
    
CREATE TABLE DimRegions (
	RegionID SMALLINT AUTO_INCREMENT PRIMARY KEY NOT NULL,
	City VARCHAR(25),
	RegionOrState VARCHAR(25),
	Country VARCHAR(25),
	Currency VARCHAR(25)
);

CREATE TABLE FactSales (
	SaleID SMALLINT AUTO_INCREMENT PRIMARY KEY NOT NULL,
	OrderDate DATE,
	ShipDate DATE,
	OrderQuantity TINYINT,
	ProductID CHAR(7) NOT NULL,
	RegionID SMALLINT NOT NULL,
    FOREIGN KEY (ProductID) REFERENCES DimProducts(ProductID),
	FOREIGN KEY (RegionID) REFERENCES DimRegions(RegionID)
);

